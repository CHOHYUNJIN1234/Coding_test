import java.util.Scanner;

public class Solution {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        char ch;
        String a = sc.next();
        for(int i=0; i<a.length(); i++){
            ch=a.charAt(i);
            System.out.println(ch);
        }
            
        
    }
}